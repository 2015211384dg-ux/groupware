// 유니코드 이모지인지 판별 (기존 저장값 호환)
function isEmoji(str) {
    if (!str) return false;
    return /\p{Emoji}/u.test(str) && !/^[a-z_]+$/.test(str);
}

export function ProjectIcon({ iconKey, size = 20, color = 'currentColor', strokeWidth = 1.6 }) {
    if (!iconKey) return null;

    // 구(舊) 이모지 값 폴백
    if (isEmoji(iconKey)) {
        return <span style={{ fontSize: size * 0.85, lineHeight: 1 }}>{iconKey}</span>;
    }

    const d = ICON_PATHS[iconKey];
    if (!d) return null;

    return (
        <svg
            width={size} height={size}
            viewBox="0 0 24 24"
            fill="none"
            stroke={color}
            strokeWidth={strokeWidth}
            strokeLinecap="round"
            strokeLinejoin="round"
        >
            {Array.isArray(d)
                ? d.map((p, i) => <path key={i} d={p} />)
                : <path d={d} />}
        </svg>
    );
}
